package Models;

public class Products {
    private String id;
    private String name;
    private double price;
    private boolean inStock;

    public Products() {}

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public double getPrice() {
        return price;
    }

    public boolean isInStock() {
        return inStock;
    }
}


